/**
 * 일기 형식 정의 — 이 PoC 실험의 중심.
 * 형식마다 대화 페르소나, 모아야 할 정보, 사진 제안 가이드, 최종 일기 생성 프롬프트가 다르다.
 * 프롬프트는 한 곳에서 관리하며 백엔드가 그대로 system/diary 프롬프트로 사용한다.
 */
export type DiaryFormat = 'plain' | 'newspaper' | 'novel';

export interface DiaryFormatDef {
  id: DiaryFormat;
  label: string;
  description: string;
  /** 대화를 이끌어가는 AI의 페르소나/시스템 프롬프트 (현재 시각은 백엔드가 덧붙임) */
  persona: string;
  /** 일기를 쓰기 위해 모아야 하는 정보 체크리스트 — AI가 이걸 채울 때까지 대화 지속 */
  requiredInfo: string[];
  /** 어떤 순간에 사진 첨부를 제안할지 */
  photoSuggestGuidance: string;
  /** 충분히 대화했다고 판단하는 기준 (offerDiaryDraft 툴 호출 시점) */
  enoughSignal: string;
  /** 최종 일기 생성용 프롬프트 (형식 톤/구조) */
  diaryPrompt: string;
}

